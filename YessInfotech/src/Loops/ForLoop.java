package Loops;

public class ForLoop {
	public static void main(String args[]) {
		System.out.print("Decrement from 100:");
		for(int i=100;i>=1;i-=20)
		{
			System.out.print(i+" ");
		}
		System.out.println();
		System.out.print("Increment from 10:");
		for(int i=10;i<=50;i+=10)
		{
			System.out.print(i+" ");
		}
	}
}
